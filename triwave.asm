;INTERFACE A DAC TO GENERATE A TRIANGULAR WAVE FORM

.MODEL SMALL
.STACK  100
.DATA
.CODE

START:  MOV AX,@DATA
	MOV DS,AX
	MOV AL,80H
MOV DX,24a3H
OUT DX,AL
L1:     MOV AL,00H
UP:     MOV DX,24a0H
	OUT DX,AL
	INC AL
	CMP AL,0FFH
	JNE UP
	MOV AL,0FFH
DOWN:   MOV DX,24a0H
	OUT DX,AL
	DEC AL
	CMP AL,00H
	JNE DOWN
	JMP L1
INT 03H
END START
